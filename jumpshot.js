		function nextQuestion() {
		
			// אם נשארה שאלה אחת לענות עליה -נחזיר אותה
			var numberOfQuestionsRemained = 0;
			var lastUnansweredQuestionIndex = -1;
		
			for (var i = 0; i < progress.length; i++) {
				if (!progress[i][0]) {
					numberOfQuestionsRemained++;
					lastUnansweredQuestionIndex = i;
				}
			}
		
			if (numberOfQuestionsRemained === 1) {
				qnum = lastUnansweredQuestionIndex;
				return; // מפסיקים את ביצוע הפונקציה
			}
		
			// אחרת נגריל שאלה מתוך השאלות שעוד לא נענו נכונה
			var previousQnum = qnum;
		
			var result = Math.floor((Math.random() * (game.length)));
		
			while (result == previousQnum || progress[result][0]) {
				result = Math.floor((Math.random() * (game.length)));
			}
		
			qnum = result;
		}
		
		function correctlyAnsweredAllQuestions() {
			return getNumberOfCorrectAnswers() == progress.length;
		}
		
		function getNumberOfCorrectAnswers() {
			var numberOfCorrectAnswers = 0;
		
			for (var i = 0; i < progress.length; i++) {
				if (progress[i][0]) {
					numberOfCorrectAnswers++;
				}
			}
		
			return numberOfCorrectAnswers;
		}
		
		function getNumberOfWrongAnswers() {
			var numberOfWrongAnswers = 0;
		
			for (var i = 0; i < progress.length; i++) {
				numberOfWrongAnswers += progress[i][1]; // מספר הנסיונות על השאלה הוא מספר התשובות הלא נכונות עליה 
			}
		
			return numberOfWrongAnswers;
		}
		
		function getScore() {
			var score = 0;
		
			for (var i = 0; i < progress.length; i++) {
				var question = progress[i];
		
				if (question[0]) { // אם ענינו נכונה
					score += 100 / (game.length * (question[1] + 1)) // מוסיפים 1 כי אנחנו סופרים את מספר השגיאות ומתחילים מ0
				}
			}
		
			return Math.floor(score);
		}
		
